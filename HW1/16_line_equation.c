#include <stdio.h>

int main() {
    int X1, Y1, X2, Y2;
    
    // Ввод координат двух точек
    scanf("%d %d %d %d", &X1, &Y1, &X2, &Y2);
    
    // Проверка на случай, если X1 равен X2 (вертикальная прямая)
    if (X1 == X2) {
        printf("Вертикальная прямая: x = %d\n", X1);
        return 0;
    }

    // Вычисление коэффициентов k и b
    double k = (double)(Y2 - Y1) / (X2 - X1);
    double b = Y1 - k * X1;

    // Вывод результата с двумя знаками после запятой
    printf("%.2f %.2f\n", k, b);
    
    return 0;
}
